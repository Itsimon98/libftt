#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*s2;
	int		a;
	int		len;

	a = 0;
	len = 0;
	s2 = malloc(len);
	len = ft_strlen(s1);
	while (s1[a] != '\0')
	{
		s2[a] = s1[a];
		a++;
	}
	return (s2);
}
